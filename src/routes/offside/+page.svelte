  <h1>Offside / Abseits</h1>

  <div class="image-placeholder">
    Bild oder Video zur Offside-Regel
  </div>

  <div class="content-box">
    <h2>Regelerklärung</h2>
    <p>
      Ein Spieler befindet sich in einer Abseitsposition, wenn er der gegnerischen Torlinie näher ist
      als der Ball und der vorletzte Gegenspieler. Entscheidend ist jedoch, ob er aktiv ins Spiel eingreift.
    </p>

    <p>
      Eine Abseitsposition alleine ist noch kein Regelverstoss. Erst wenn der Spieler den Ball spielt,
      einen Gegner beeinflusst oder aus seiner Position einen Vorteil zieht, wird Offside gepfiffen.
    </p>
  </div>

  <div class="content-box">
    <h2>Beispiel aus dem Spiel</h2>
    <p>
      Ein Stürmer steht hinter der Verteidigung und bekommt einen Pass von einem Mitspieler.
      Wenn er beim Moment des Passes näher zur Torlinie steht als der vorletzte Verteidiger,
      kann dies Offside sein.
    </p>
  </div>
</section>

